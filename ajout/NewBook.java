    private void btnAjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAjouterActionPerformed
        // TODO add your handling code here:
        
        
            String code = textCode.getText();
        String nom =  textName.getText();
        String acqui =  textAcquisition.getText();
        String retour = textBack.getText();
        String sale = textSalle.getText();
        String idLib = textIdLibrary.getText();
        
        if(nom.isEmpty() || acqui.isEmpty() || retour.isEmpty() || sale.isEmpty() || idLib.isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Veuillez remplir tous les champs", "Ressayez SVP!",JOptionPane.ERROR_MESSAGE);
        }
        else
        {
            DefaultTableModel model = (DefaultTableModel) tableSubs.getModel();
            model.addRow(new Object []{ nom, acqui, retour, sale, idLib});
        }
        
        
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/GestionBibliotheque?useSSL=true","root","");
            
            pst = con.prepareStatement("INSERT INTO LIVRE (CodeBar, Nom, DateAcquisition, DatePrevue, Sale,ID_Librairie) VALUES (?,?,?,?,?,?)");
            
            pst.setString(1, code);
            pst.setString(2, nom);
            pst.setString(3, acqui);
            pst.setString(4, retour);
            pst.setString(5, sale);
            pst.setString(6, idLib);
            
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "livre ajout√© !");
            } catch (ClassNotFoundException ex) {
            Logger.getLogger(NewBook.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(NewBook.class.getName()).log(Level.SEVERE, null, ex);
        }
            
    }//GEN-LAST:event_btnAjouterActionPerformed

